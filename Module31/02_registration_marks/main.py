import re

print('Задача 2. Регистрационные знаки')
user_nums = ('А578ВЕ777 '
             'ОР233787 '
             'К901МН666 '
             'СТ46599 '
             'СНИ2929П777 '
             '666АМР666')

print(f'Список номеров частных автомобилей: '
      f'{re.findall(r'\b[АВЕКМНОРСТУХ]{1}\d{3}[АВЕКМНОРСТУХ]{2}\d{2,3}',
                    user_nums)}')
print(f'Список номеров такси: '
      f'{re.findall(r'\b[АВЕКМНОРСТУХ]{2}\d{3}\d{2,3}', user_nums)}')
